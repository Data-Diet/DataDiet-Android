package jhmanalo.example.datadiet;

public class Product {
    String name, url, warnings, labels;

    String getName() { return name; }
    void setName(String name) { this.name = name; }
    String getUrl() { return url; }
    void setUrl(String url) { this.url = url; }
    String getWarnings() { return warnings; }
    void setWarnings(String warnings) { this.warnings = warnings; }
    String getLabels() { return labels; }
    void setLabels(String labels) { this.labels = labels; }
}
